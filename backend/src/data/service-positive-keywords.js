export const SERVICE_POSITIVE_KEYWORDS_BY_NAME = Object.freeze(
  Object.fromEntries(
    Object.entries(rawServicePositiveKeywordsByName).map(([serviceName, keywords]) => [
      serviceName,
      Object.freeze([...keywords]),
    ]),
  ),
);

export const getServicePositiveKeywordsByName = (serviceName = "") => {
  const normalizedServiceName = String(serviceName || "").trim();
  const keywords = SERVICE_POSITIVE_KEYWORDS_BY_NAME[normalizedServiceName];
  return Array.isArray(keywords) ? [...keywords] : [];
};

export const seedServicePositiveKeywords = async (
  prismaClient,
  { serviceNames = [] } = {},
) => {
  const availableServices = await prismaClient.marketplaceFilterService.findMany({
    select: { id: true, name: true },
  });
  const serviceIdByName = new Map(
    availableServices.map((service) => [String(service.name || "").trim(), service.id]),
  );
  const requestedServiceNames =
    Array.isArray(serviceNames) && serviceNames.length > 0
      ? serviceNames
      : Object.keys(SERVICE_POSITIVE_KEYWORDS_BY_NAME);

  const uniqueTargetServiceNames = Array.from(
    new Set(
      requestedServiceNames
        .map((serviceName) => String(serviceName || "").trim())
        .filter(Boolean),
    ),
  );

  let seededCount = 0;
  let matchedServiceCount = 0;

  for (const serviceName of uniqueTargetServiceNames) {
    const serviceId = serviceIdByName.get(serviceName);
    if (!serviceId) {
      continue;
    }

    matchedServiceCount += 1;
    const keywords = getServicePositiveKeywordsByName(serviceName);

    for (const keywordName of keywords) {
      await prismaClient.servicePositiveKeyword.upsert({
        where: {
          serviceId_name: {
            serviceId,
            name: keywordName,
          },
        },
        update: {},
        create: {
          serviceId,
          name: keywordName,
        },
      });
      seededCount += 1;
    }
  }

  return {
    seededCount,
    matchedServiceCount,
    missingServiceNames: uniqueTargetServiceNames.filter(
      (serviceName) => !serviceIdByName.has(serviceName),
    ),
  };
};
